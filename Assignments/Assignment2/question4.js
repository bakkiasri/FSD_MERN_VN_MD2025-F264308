let Fruits = ["Apple", "Orange", "Grapes", "Jack Fruits", "pine Apple"];

Fruits.push("Mango"); // Adds Mango
console.log(Fruits);

Fruits.pop(); // Removes last item (Mango)
console.log(Fruits);

Fruits.unshift("Black berry"); // Add at start
console.log(Fruits);

Fruits.shift(); // Remove first item (Black berry)
console.log(Fruits);
